import math

def alcuin(n):
      # Edit: The sequence begins with the coefficient of x^0, first term requires n = 0
    return round((n * n) / 12) - math.floor(n / 4) * math.floor((n + 2) / 4)
def triangles(n):
    if n % 2 == 0:
        return alcuin(n) + n//4
    else:
        return alcuin(n)

